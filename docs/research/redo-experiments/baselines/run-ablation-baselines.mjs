import fs from "node:fs";
import path from "node:path";
import crypto from "node:crypto";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const redoRoot = path.resolve(scriptDir, "..");
const reportPath = path.join(redoRoot, "reports", "ablation-baseline-summary.json");

function main() {
  const generatedAt = new Date().toISOString();
  const authorizationCases = buildAuthorizationCases();
  const credentialCases = buildCredentialCases();
  const freshnessCases = buildFreshnessCases();

  const authorizationOutcomes = authorizationCases.map((item) => ({
    ...item,
    fullModelAccepted: fullAuthorizationDecision(item),
    degradedAccepted: degradedAuthorizationDecision(item),
    fullReason: fullAuthorizationDecision(item) ? "authorized_domain_match" : "unauthorized_domain",
    degradedReason: "capability_query_match_without_domain_filter",
  }));
  const credentialOutcomes = credentialCases.map((item) => ({
    ...item,
    fullModelAccepted: fullCredentialDecision(item),
    degradedAccepted: degradedCredentialDecision(item),
    fullReason: fullCredentialDecision(item) ? "credential_valid" : item.fault,
    degradedReason: "credential_verification_removed",
  }));
  const freshnessOutcomes = freshnessCases.map((item) => ({
    ...item,
    fullModelAccepted: fullFreshnessDecision(item),
    degradedAccepted: degradedFreshnessDecision(item),
    fullReason: fullFreshnessDecision(item) ? "fresh_invocation" : item.fault,
    degradedReason: "freshness_validation_removed",
  }));

  const authorization = summarizeExposure(
    "no authorization-aware discovery filtering",
    authorizationOutcomes,
    (item) => item.expectedAuthorized,
  );
  authorization.inputAgents = authorizationCases.length;
  authorization.authorizedAgents = authorizationCases.filter((item) => item.expectedAuthorized).length;
  authorization.unauthorizedAgents = authorizationCases.filter((item) => !item.expectedAuthorized).length;
  authorization.outcomes = authorizationOutcomes;

  const credential = summarizeFalseAcceptance(
    "no registration-credential verification",
    credentialOutcomes,
  );
  credential.caseTypes = credentialCases.map((item) => item.fault);
  credential.outcomes = credentialOutcomes;

  const freshness = summarizeFalseAcceptance(
    "no pre-connection freshness validation",
    freshnessOutcomes,
  );
  freshness.caseTypes = freshnessCases.map((item) => item.fault);
  freshness.outcomes = freshnessOutcomes;

  const summary = {
    experimentName: "internal-degraded-baseline-ablation",
    generatedAt,
    method:
      "mechanism-level degraded internal baselines generated by executing full and degraded decision functions over the same input cases",
    results: {
      authorizationFiltering: authorization,
      registrationCredential: credential,
      preConnectionFreshness: freshness,
    },
    tableRows: [
      row(
        "Authorization-aware discovery filtering",
        authorization.unauthorizedAgents,
        authorization.fullModel.unauthorizedExposureCount,
        authorization.degradedBaseline.unauthorizedExposureCount,
        "authorization-scoped discovery",
      ),
      row(
        "Registration-credential verification",
        credential.invalidCases,
        credential.fullModel.falseAcceptances,
        credential.degradedBaseline.falseAcceptances,
        "registration admissibility",
      ),
      row(
        "Pre-connection freshness validation",
        freshness.invalidCases,
        freshness.fullModel.falseAcceptances,
        freshness.degradedBaseline.falseAcceptances,
        "invocation freshness",
      ),
    ],
  };
  fs.mkdirSync(path.dirname(reportPath), { recursive: true });
  fs.writeFileSync(reportPath, `${JSON.stringify(summary, null, 2)}\n`, "utf8");
  console.log(reportPath);
}

function buildAuthorizationCases() {
  const authorizedDomains = ["gbt4754-2017.01"];
  return Array.from({ length: 24 }, (_, index) => {
    const expectedAuthorized = index < 12;
    const primaryTag = expectedAuthorized ? "gbt4754-2017.01" : "gbt4754-2017.02";
    return {
      id: `authz-${index + 1}`,
      queryTag: "ablation.shared",
      capabilityTags: [primaryTag, "ablation.shared"],
      authorizedDomains,
      expectedAuthorized,
    };
  });
}

function buildCredentialCases() {
  return [
    "tampered credential signature",
    "wrong credential subject",
    "wrong credential issuer",
    "inactive credential status",
    "registration binding hash mismatch",
    "registration binding proof-hash mismatch",
  ].map((fault, index) => ({
    id: `credential-${index + 1}`,
    agentDid: `did:ans:AGBM:${crypto.createHash("sha256").update(fault).digest("hex").slice(0, 16)}`,
    fault,
    credentialSignatureValid: fault !== "tampered credential signature",
    subjectMatches: fault !== "wrong credential subject",
    issuerMatches: fault !== "wrong credential issuer",
    statusActive: fault !== "inactive credential status",
    bindingHashMatches: fault !== "registration binding hash mismatch",
    bindingProofHashMatches: fault !== "registration binding proof-hash mismatch",
  }));
}

function buildFreshnessCases() {
  const nowMs = Date.parse("2026-05-31T08:00:00Z");
  return [
    ["replayed nonce 1", { noncePreviouslySeen: true, timestampMs: nowMs - 1_000 }],
    ["replayed nonce 2", { noncePreviouslySeen: true, timestampMs: nowMs - 2_000 }],
    ["replayed nonce 3", { noncePreviouslySeen: true, timestampMs: nowMs - 3_000 }],
    ["replayed nonce 4", { noncePreviouslySeen: true, timestampMs: nowMs - 4_000 }],
    ["expired timestamp 1", { noncePreviouslySeen: false, timestampMs: nowMs - 600_000 }],
    ["expired timestamp 2", { noncePreviouslySeen: false, timestampMs: nowMs - 900_000 }],
    ["future timestamp 1", { noncePreviouslySeen: false, timestampMs: nowMs + 600_000 }],
    ["timestamp without timezone", { noncePreviouslySeen: false, timestampMs: nowMs, timestampHasTimezone: false }],
  ].map(([fault, values], index) => ({
    id: `freshness-${index + 1}`,
    fault,
    nowMs,
    timestampHasTimezone: true,
    ...values,
  }));
}

function fullAuthorizationDecision(item) {
  const queryMatches = item.capabilityTags.includes(item.queryTag);
  const domainMatches = item.authorizedDomains.some((domain) =>
    item.capabilityTags.some((tag) => tag === domain || tag.startsWith(`${domain}.`)),
  );
  return queryMatches && domainMatches;
}

function degradedAuthorizationDecision(item) {
  return item.capabilityTags.includes(item.queryTag);
}

function fullCredentialDecision(item) {
  return (
    item.credentialSignatureValid &&
    item.subjectMatches &&
    item.issuerMatches &&
    item.statusActive &&
    item.bindingHashMatches &&
    item.bindingProofHashMatches
  );
}

function degradedCredentialDecision() {
  return true;
}

function fullFreshnessDecision(item) {
  const maxSkewMs = 5 * 60 * 1_000;
  const timestampFresh = Math.abs(item.nowMs - item.timestampMs) <= maxSkewMs;
  return item.timestampHasTimezone !== false && timestampFresh && !item.noncePreviouslySeen;
}

function degradedFreshnessDecision() {
  return true;
}

function summarizeExposure(name, outcomes, isAuthorized) {
  const unauthorized = outcomes.filter((item) => !isAuthorized(item));
  const fullExposure = unauthorized.filter((item) => item.fullModelAccepted).length;
  const degradedExposure = unauthorized.filter((item) => item.degradedAccepted).length;
  return {
    fullModel: {
      returnedAgents: outcomes.filter((item) => item.fullModelAccepted).length,
      unauthorizedExposureCount: fullExposure,
      falseExposureRate: fullExposure / unauthorized.length,
      policyEnforcementAccuracy: outcomes.filter((item) => item.fullModelAccepted === isAuthorized(item)).length / outcomes.length,
      acceptedAuthorizedCount: outcomes.filter((item) => isAuthorized(item) && item.fullModelAccepted).length,
      rejectedUnauthorizedCount: unauthorized.filter((item) => !item.fullModelAccepted).length,
    },
    degradedBaseline: {
      name,
      returnedAgents: outcomes.filter((item) => item.degradedAccepted).length,
      unauthorizedExposureCount: degradedExposure,
      falseExposureRate: degradedExposure / unauthorized.length,
      policyEnforcementAccuracy: outcomes.filter((item) => item.degradedAccepted === isAuthorized(item)).length / outcomes.length,
    },
  };
}

function summarizeFalseAcceptance(name, outcomes) {
  const fullFalseAcceptances = outcomes.filter((item) => item.fullModelAccepted).length;
  const degradedFalseAcceptances = outcomes.filter((item) => item.degradedAccepted).length;
  return {
    invalidCases: outcomes.length,
    fullModel: {
      falseAcceptances: fullFalseAcceptances,
      falseAcceptanceRate: fullFalseAcceptances / outcomes.length,
      rejectedCases: outcomes.length - fullFalseAcceptances,
    },
    degradedBaseline: {
      name,
      falseAcceptances: degradedFalseAcceptances,
      falseAcceptanceRate: degradedFalseAcceptances / outcomes.length,
      rejectedCases: outcomes.length - degradedFalseAcceptances,
    },
  };
}

function row(
  removedMechanism,
  invalidOrUnauthorizedCases,
  fullModelFalseAcceptanceOrExposure,
  degradedFalseAcceptanceOrExposure,
  protectedProperty,
) {
  return {
    removedMechanism,
    invalidOrUnauthorizedCases,
    fullModelFalseAcceptanceOrExposure,
    degradedFalseAcceptanceOrExposure,
    fullModelRate: fullModelFalseAcceptanceOrExposure / invalidOrUnauthorizedCases,
    degradedRate: degradedFalseAcceptanceOrExposure / invalidOrUnauthorizedCases,
    protectedProperty,
  };
}

main();
